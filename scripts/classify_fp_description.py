def classify(desc: str) -> str:
    if pd.isna(desc):
        return "UNKNOWN"
    d = str(desc).lower().strip()
    for cls, patterns in RULES:
        for pat in patterns:
            if re.search(pat, d):
                return cls
    return "UNKNOWN"


